import { customAlphabet } from "nanoid";

// nanoid's default alphabet is URL-safe (alphanumerics + `-` + `_`). We
// drop both punctuation chars: `-` collides with parsers that treat dashes
// as field separators, and `_` looks doubled against the literal prefix
// (`hydra_session__foo`). Plain alphanumeric is plenty of entropy at
// length 16 (~95 bits).
const HYDRA_ID_ALPHABET =
  "0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz";
const generateHydraId = customAlphabet(HYDRA_ID_ALPHABET, 16);

export const HYDRA_SESSION_PREFIX = "hydra_session_";

// Stable id stamped onto prompt_received / turn_complete updates per
// RFD #533. Lets reconnecting clients pass `after_message: <id>` to
// resync only the delta. 16 chars of alphanumeric is ~95 bits — plenty.
function generateMessageId(): string {
  return `m_${generateHydraId()}`;
}

// Display helper: strip the constant prefix from auto-generated session ids
// so the random tail is what users see in lists and pickers. Foreign ids
// (anything not generated by us) pass through untouched.
export function stripHydraSessionPrefix(id: string): string {
  return id.startsWith(HYDRA_SESSION_PREFIX)
    ? id.slice(HYDRA_SESSION_PREFIX.length)
    : id;
}
