  // usage_update carries any subset of {used, size, cost.amount,
  // cost.currency}. Merge non-undefined fields onto currentUsage so a
  // sparse update preserves prior values, and fire usage handlers only
  // if something actually changed.
  private maybeApplyAgentUsage(params: unknown): boolean {
    const obj = (params ?? {}) as { update?: unknown };
    const update = (obj.update ?? {}) as {
      sessionUpdate?: unknown;
      used?: unknown;
      size?: unknown;
      cost?: unknown;
    };
    if (update.sessionUpdate !== "usage_update") {
      return false;
    }
    const next: UsageSnapshot = { ...(this._currentUsage ?? {}) };
    let changed = false;
    if (typeof update.used === "number" && next.used !== update.used) {
      next.used = update.used;
      changed = true;
    }
    if (typeof update.size === "number" && next.size !== update.size) {
      next.size = update.size;
      changed = true;
    }
    if (update.cost && typeof update.cost === "object") {
      const cost = update.cost as { amount?: unknown; currency?: unknown };
      if (typeof cost.amount === "number" && next.costAmount !== cost.amount) {
        next.costAmount = cost.amount;
        changed = true;
      }
      if (
        typeof cost.currency === "string" &&
        next.costCurrency !== cost.currency
      ) {
        next.costCurrency = cost.currency;
        changed = true;
      }
    }
    if (!changed) {
      return true;
    }
    this._currentUsage = next;
    // Fire handlers with the total (getter) not the raw snapshot so that
    // meta.json always persists the accumulated costAmount. If we fire with
    // the raw `next` (no cumulativeCost), a daemon restart would reconstruct
    // cumulativeCost as 0 + raw instead of the true lifetime total.
    const total = this.currentUsage ?? next;
    for (const handler of this.usageHandlers) {
      try {
        handler(total);
      } catch {
        void 0;
      }
    }
    return true;
  }

  // Move currentUsage.costAmount into cumulativeCost and clear it so the
  // next agent life starts accumulating from $0. Fires usageHandlers so
  // meta.json is updated before the new agent starts emitting.
  private accumulateAndResetCost(): void {
    const amount = this._currentUsage?.costAmount;
    if (!amount)
      return;
    this.cumulativeCost += amount;
    const next: UsageSnapshot = {
      ...(this._currentUsage ?? {}),
      cumulativeCost: this.cumulativeCost,
      costAmount: undefined,
    };
    this._currentUsage = next;
    for (const handler of this.usageHandlers) {
      try {
        handler(next);
      } catch {
        void 0;
      }
    }
  }

  // Returns a modified envelope with cost.amount replaced by the running
  // total (cumulativeCost + raw agent amount). No-op if the envelope
  // doesn't carry a numeric cost.amount.
  private injectCumulativeCost(envelope: unknown): unknown {
    if (!this.cumulativeCost)
      return envelope;
    if (!envelope || typeof envelope !== "object")
      return envelope;
    const obj = envelope as Record<string, unknown>;
    if (!obj.update || typeof obj.update !== "object")
      return envelope;
    const update = obj.update as Record<string, unknown>;
    if (update.sessionUpdate !== "usage_update")
      return envelope;
    if (!update.cost || typeof update.cost !== "object")
      return envelope;
    const cost = update.cost as Record<string, unknown>;
    if (typeof cost.amount !== "number")
      return envelope;
    return {
      ...obj,
      update: {
        ...update,
        cost: { ...cost, amount: this.cumulativeCost + cost.amount },
      },
    };
  }

