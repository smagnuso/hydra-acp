  // First-prompt heuristic: derive a session title from the first
  // session/prompt's text. Replaces whatever was set at session/new
  // (typically an editor frame name like "Claude Agent @ hydra-acp")
  // — the first prompt is a better summary for cross-client display
  // than the editor's static frame label. Subsequent prompts don't
  // touch the title; that'd flap as conversations evolved.
  private maybeSeedTitleFromPrompt(params: unknown): void {
    if (this.firstPromptSeeded) {
      return;
    }
    const promptParams = (params ?? {}) as { prompt?: unknown };
    const text = extractPromptText(promptParams.prompt);
    const seed = firstLine(text, 200);
    if (!seed) {
      return;
    }
    // Skip slash commands as title seeds. `/hydra planner create`,
    // `/model gpt-5`, etc. are administrative or routing prompts —
    // not the conversational summary a session title is meant to
    // capture. Subsequent non-slash prompts can still seed (the
    // _firstPromptSeeded flag stays false for slash-only flows so
    // the next real prompt wins).
    if (seed.startsWith("/")) {
      return;
    }
    this._firstPromptSeeded = true;
    this.setTitle(seed);
  }

  // Apply an agent-emitted current_model_update. Returns true if the
  // notification was a model update (caller still needs to broadcast
  // it). Returns false otherwise so the caller can try the next kind.
  // current_model_update can carry availableModels in the same payload
  // (per ACP spec); we cache that list too so session/set_model can
  // validate against it.
  private maybeApplyAgentModel(params: unknown): boolean {
    const obj = (params ?? {}) as { update?: unknown };
    const update = (obj.update ?? {}) as {
      sessionUpdate?: unknown;
      currentModel?: unknown;
      model?: unknown;
      availableModels?: unknown;
    };
    if (update.sessionUpdate !== "current_model_update") {
      return false;
    }
    const models = parseModelsList(update.availableModels);
    if (models.length > 0) {
      this.setAgentAdvertisedModels(models);
    }
    const raw =
      typeof update.currentModel === "string"
        ? update.currentModel
        : typeof update.model === "string"
        ? update.model
        : undefined;
    if (raw === undefined) {
      return true;
    }
    const trimmed = raw.trim();
    if (!trimmed || trimmed === this.currentModel) {
      return true;
    }
    this.logger?.info(
      `live current_model_update: sessionId=${this.sessionId} ${JSON.stringify(this.currentModel)} → ${JSON.stringify(trimmed)}`,
    );
    this.currentModel = trimmed;
    for (const handler of this.modelHandlers) {
      try {
        handler(trimmed);
      } catch {
        void 0;
      }
    }
    this.broadcastConfigOptions();
    return true;
  }

  // Apply an agent-emitted config_option_update. claude-acp and opencode
  // emit this (not the spec-shaped current_model_update /
  // available_modes_update) to carry the current value AND option list for
  // model and mode. The payload is `configOptions: [{ id, currentValue,
  // options: [{ value, name }] }, ...]`. We harvest the "model" and "mode"
  // entries — other ids (e.g. "effort") are agent-internal and ignored.
  // Harvesting the mode list here is what populates availableModes for
  // agents that never send available_modes_update (so the TUI's Shift+Tab
  // cycle has something to cycle through). Returns true when recognized so
  // the wireAgent loop stops trying further extractors (the original frame
  // still broadcasts; config-options-aware clients render it directly).
  private maybeApplyAgentConfigOption(params: unknown): boolean {
    const obj = (params ?? {}) as { update?: unknown };
    const update = (obj.update ?? {}) as {
      sessionUpdate?: unknown;
      configOptions?: unknown;
    };
    if (update.sessionUpdate !== "config_option_update") {
      return false;
    }
    const list = update.configOptions;
    if (!Array.isArray(list)) {
      return true;
    }
    for (const raw of list) {
      if (!raw || typeof raw !== "object") {
        continue;
      }
      const opt = raw as {
        id?: unknown;
        currentValue?: unknown;
        options?: unknown;
      };
      if (opt.id === "model") {
        const models = parseModelsList(opt.options);
        if (models.length > 0) {
          this.setAgentAdvertisedModels(models);
        }
        const cv = opt.currentValue;
        if (typeof cv === "string") {
          const trimmed = cv.trim();
          if (trimmed && trimmed !== this.currentModel) {
            this.logger?.info(
              `live config_option_update(model): sessionId=${this.sessionId} ${JSON.stringify(this.currentModel)} → ${JSON.stringify(trimmed)}`,
            );
            // Delegate to applyModelChange so the daemon also emits a
            // spec-shaped current_model_update. claude-acp/opencode carry
            // the new model only in this non-spec config_option_update;
            // clients that don't render it (notably the TUI) would
            // otherwise stay pinned to the agent's earlier stale value.
            this.applyModelChange(trimmed);
          }
        }
      } else if (opt.id === "mode") {
        const modes = parseModesList(opt.options);
        if (modes.length > 0) {
          this.setAgentAdvertisedModes(modes);
        }
        const cv = opt.currentValue;
        if (typeof cv === "string") {
          const trimmed = cv.trim();
          if (trimmed && trimmed !== this.currentMode) {
            this.logger?.info(
              `live config_option_update(mode): sessionId=${this.sessionId} ${JSON.stringify(this.currentMode)} → ${JSON.stringify(trimmed)}`,
            );
            // Mirror the model branch: emit a spec-shaped
            // current_mode_update so mode-only clients (the TUI) repaint.
            this.applyModeChange(trimmed);
          }
        }
      } else if (typeof opt.id === "string" && opt.id.trim()) {
        // Non-standard config option (e.g. "effort", "thought_level").
        // Store it so buildConfigOptions can surface it alongside
        // model/mode/agent — agents advertise these only in the
        // config_option_update payload, never via a dedicated channel.
        const id = opt.id.trim();
        const options = parseModelsList(opt.options);
        if (options.length > 0) {
          const modeList = parseModesList(opt.options);
          const valueOpts: ConfigOptionValue[] = modeList.length > 0
            ? modeList.map((m) => ({
                value: m.id,
                name: m.name ?? m.id,
              }))
            : options.map((m) => ({
                value: m.modelId,
                name: m.name ?? m.modelId,
              }));
          const rawName = (opt as { name?: unknown }).name;
          const name = typeof rawName === "string" && rawName.trim() ? rawName.trim() : id;
          const rawCv = opt.currentValue;
          const currentValue =
            typeof rawCv === "string" && rawCv.trim()
              ? rawCv.trim()
              : valueOpts[0]?.value ?? "";
          const existing = this.agentAdvertisedConfigOptions.findIndex(
            (o) => o.id === id,
          );
          if (existing >= 0) {
            const updated = { ...this.agentAdvertisedConfigOptions[existing]! };
            updated.options = valueOpts;
            if (typeof rawCv === "string" && rawCv.trim()) {
              updated.currentValue = rawCv.trim();
            }
            this.agentAdvertisedConfigOptions[existing] = updated;
          } else {
            this.agentAdvertisedConfigOptions.push({
              id,
              name,
              category: "other",
              type: "select",
              currentValue,
              options: valueOpts,
            });
          }
        }
      }
    }
    return true;
  }

