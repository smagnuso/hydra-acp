// How long the daemon waits for a transformer that returned "processing" to
// discharge its claim before synthesizing a stop response.
const TRANSFORMER_CLAIM_TIMEOUT_MS = 5 * 60 * 1000; // 5 minutes

interface TransformerClaim {
  resolve: (result: unknown) => void;
  timer: NodeJS.Timeout;
  transformerName: string;
  method: string;
  // Enough state to resume the chain fail-open if the transformer abandons.
  envelope: unknown;
  chainIdx: number;           // index of the transformer that claimed processing
  originatedBy: Set<string>;  // snapshot so resume routing is correct
  side: "request" | "response";
  // For request-side claims, whether the chain's tail dispatches as an
  // agent request or as an agent notification. Used by the keep-alive
  // fail-open path to resume the chain with the correct tail. Unused
  // for response-side claims.
  tailKind?: "request" | "notification";
}

// Cap on the recently-terminal messageId LRU. Bounds memory growth on
// a long-running session; older entries fall out and resolve to
// target_not_found, which is the correct behavior for an amend that
// targets a turn from minutes ago.
const RECENTLY_TERMINAL_LIMIT = 64;

// Two flavors of queue entry share the same FIFO. User-prompt entries
// are wire-visible (every transition fires a hydra-acp/prompt_queue_*
// broadcast); internal entries (title regen, agent switch, import seed)
// serialize through the same gate but are invisible to clients — they
// look like a brief moment of busyness from outside.
interface UserPromptQueueEntry {
  kind: "user";
  messageId: string;
  originator: PromptOriginator;
  // clientId is the original sender's id (== originator.clientId), kept
  // separately so broadcastTurnComplete keeps its "exclude originator"
  // semantics even after the entry has been spliced out of the queue.
  clientId: string;
  // The session/prompt prompt array, kept mutable so updateQueuedPrompt
  // can swap it before drainQueue picks the entry up.
  prompt: unknown[];
  enqueuedAt: number;
  cancelled: boolean;
  // True when this entry was created via hydra-acp/prompt/amend rather
  // than session/prompt. The originator has no awaiting session/prompt
  // promise to receive a stopReason on; broadcastTurnComplete therefore
  // includes the originator in the wire turn_complete (no exclusion) so
  // their TUI can clear currentHeadMessageId and adjust pendingTurns.
  wasAmend?: boolean;
  resolve: (value: unknown) => void;
  reject: (err: Error) => void;
}

interface InternalQueueEntry {
  kind: "internal";
  messageId: string;
  enqueuedAt: number;
  cancelled: boolean;
  task: () => Promise<unknown>;
  resolve: (value: unknown) => void;
  reject: (err: Error) => void;
}

type QueueEntry = UserPromptQueueEntry | InternalQueueEntry;

